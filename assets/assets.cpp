static void draw_disc(uint16_t* sprite, int cx, int cy, int r, uint16_t color) {
    for (int y = 0; y < 16; ++y) {
        for (int x = 0; x < 16; ++x) {
            int dx = x - cx, dy = y - cy;
            if (dx*dx + dy*dy <= r*r) {
                sprite[y*16 + x] = color;
            }
        }
    }
}

